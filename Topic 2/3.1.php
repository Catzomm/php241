<!-- 5. Запись и чтение из файла. Пусть в корне вашего сайта лежит файл count.txt. 
 Изначально в нем записано число 0. Сделайте так, чтобы при обновлении страницы наш скрипт каждый раз увеличивал это число на 1. 
 То есть у нас получится счетчик обновления страницы в виде файла. -->
 <?php
$file = 'count.txt';

// Если файла нет, создаем и записываем 0
if (!file_exists($file)) {
    file_put_contents($file, '0');
}

// Читаем текущее значение
$count = (int)file_get_contents($file);
// Увеличиваем на 1
$count++;
// Записываем обратно
file_put_contents($file, $count);

echo "Количество обновлений страницы: $count";
?>

